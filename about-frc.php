					<p><em>FIRST</em><sup>&reg;</sup> Robotics Competition (FRC) is an international high school robotics competition. Each year, teams of high school students and mentors work during a six-week period to build game-playing robots that weigh up to 120 pounds (54 kg). Robots complete tasks such as scoring balls into goals, Frisbees into goals, inner tubes onto racks, hanging on bars, and balancing robots on balance beams, for example this years "challenge" was <a class="body-link" href="https://www.youtube.com/watch?v=W6UYFKNGHJ8">Recycle Rush</a>. The game changes yearly, keeping the excitement fresh and giving each team a more level playing field. While teams are given a standard set of parts, they are also allowed a budget and encouraged to buy or make specialized parts. FRC is one of four robotics competition programs organized by <em>FIRST</em>.</p>
					
					<p>FRC has a unique culture, built around two values. Gracious Professionalism embraces the competition inherent in the program, but rejects trash talk and chest-thumping, instead embracing empathy and respect for other teams. Cooperation between teams emphasizes that teams can cooperate and compete at the same time. The goal of the program is to inspire students to be science and technology leaders and foster a friendly .In addition to on-field competition, teams and team members compete for awards recognizing entrepreneurship, creativity, engineering, industrial design, safety, controls, media, quality, and exemplifying the core values of the program. Most teams reside in the United States, with Canada, Israel, and Mexico contributing significant numbers of teams.</p>
